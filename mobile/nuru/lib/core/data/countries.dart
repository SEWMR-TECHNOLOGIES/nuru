/// Country data model with flag emoji, dial code, and name
class CountryData {
  final String code;
  final String name;
  final String dialCode;

  const CountryData({
    required this.code,
    required this.name,
    required this.dialCode,
  });

  /// Generate flag emoji dynamically from ISO country code
  String get flag {
    return code.toUpperCase().runes.map((r) {
      return String.fromCharCode(0x1F1E6 + r - 0x41);
    }).join();
  }
}

